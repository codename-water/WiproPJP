a=int(input("Enter value of a : "))
b=int(input("Enter value of b : "))

add=a+b           #addition
sub=a-b           #subtraction
mul=a*b           #multiplication
float_div=a/b     #division, result is float
floor_div=a//b    #division, result is floor
mod=a%b           #modulo

print("Addition: ",add)
print("Subtraction: ",sub)
print("Multiplication: ",mul)
print("Division (float value): ",float_div)
print("Division (floor value): ",floor_div)
print("Modulo: ",mod)
